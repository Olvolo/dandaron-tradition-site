                        <h1 class="text-center">Заголовок вашей статьи</h1>
                        <p>Это параграф с <strong>жирным</strong> и <em>курсивным</em> текстом.</p>

                        <p>Буддизм рассматривает вопрос сансары, ее страданий и путь освобождения
                            от нее, то есть путь индивида до погружения его в нирвану, и определяет
                            состояние нирваны как конечный пункт cовершенствующего движения
                            Алая-виджняны<a href="#citation-7" id="ref-7"
                                            class="citation-mark"><sup> 7</sup></a>. Поэтому наше
                            знание ограничивается пределами сансары и проявлением нирваны в ее же
                            пределах, то есть в пределах нашей собственной системы времени и
                            пространства, а потому – физическим мировым порядком, в котором мы
                            движемся. О времени или периоде до всякого времени мы полагаем, что его
                            содержание и характер нам неизвестны.</p>
                        <p>Пути Господни неисповедимы!</p>
                        <p>В те времена, когда существовало абсолютное время или чистая
                            длительность, текущая равномерно и независимо от каких бы то ни было
                            совершенствующих процессов (или процессы протекали над временем), был
                            великий дух, самодовлеющий, вместилищем его была <strong>шунья</strong>
                            (ничто с возможностью нечто). Он узрел необходимость совершенного
                            проявления и выделил несовершенную часть из своего тела (состояния); как
                            утверждает Великий йог Нацог-Рандол (sna tshogs rang 'grol) в
                            своем сочинении «Карнатантра» (snyun brgyud), совершенный дух, познавший
                            свою сущность<em>, ушел</em> от несовершенного (непознавшего свою
                            сущность). Здесь образовалась точка расхождения Сансары и Нирваны по
                            причине того, что Нирвана не могла сосуществовать с Сансарой ввиду
                            необходимости созидания последней. Необходимое соединение ее с Нирваной
                            должно осуществиться только посредством полного совершенствования,
                            просветления Сансары. Итак, совершенный дух (Нирвана, или Адибудда),
                            отделяясь и уходя от непросветленного вследствие необходимого
                            творчества, создал <em>движение</em>, которое является одним из основных
                            моментов созидания. Одновременно с возникновением движения возникает
                            начало времени и пространства, которые являются координатами, условиями
                            существования движения. На следующем этапе произошло самовозниковение
                            <em>праматерии</em>. Предыдущий этап был этапом чистого движения
                            Алая-виджняны, которая является частью всеобщего – «Алая». Поэтому как
                            частица Адибудды (божества) она постоянно, как тень, как волны на
                            поверхности воды, стремящиеся за идущей лодкой, стремится к Нирване, к
                            первоначальному своему состоянию. Возникновение праматерии
                            сопровождается необходимостью слияния ее с Алая-виджняной. Воплощение
                            непросветленной виджняны в пространственно-материальный образ
                            (первоматерию) как первая ступень саморазвития непросветленной частицы
                            абсолютного духа (Дхармакаи-Адибудды), составляет первоначало всего
                            бытия и устанавливает закон вечного совершенствования, развернувшегося
                            во времени, изменение которого характеризуется равномерно возрастающим
                            числовым рядом. причем изменение во времени происходит в одном
                            направлении: от прошедшего к будущему; то есть цикл развития отражает
                            постоянное стремление несовершенной Алая-виджняны к Нирване (Дхармакае)
                            и течет по восходящей линии.</p>
                        <p>Это бесконечное совершенствование Алая-виджняны (духа) происходит из
                            бесчисленных фаз саморазвития, необходимым результатом которого является
                            взаимодействие множества причинно-следственных рядов. Если в
                            феноменальном мире совершенствующийся дух (виджняна) являет собой лишь
                            потенцию (возможность) бытия, еще неосуществленного (неочищенного) в
                            себе бытия Абсолюта (Нирваны), и если, реализуясь, виджняна по закону
                            причин и следствий переходит из стадии возможности в инобытие, то есть в
                            бессознательное бытие живой природы, то на следующей ступени
                            саморазвития совершенствующаяся Алая-виджняна обретает сознательность в
                            камадхату ('dod khams) в человеческом индивиде, асуре и
                            небожителе, а также в сферах рупадхату (gzugs khams) и арупадхату (gzugs
                            med khams) в виде высоких духов. Эпоха самосовершенствования
                            непросветленной виджняны через материальный мир в буддизме носит
                            название <strong>Сансара</strong>. Это вечное стремление виджняны к
                            совершенству и движение, обусловленное этим, происходят по восходящей
                            линии только через материальный мир и в материальном мире. При этом тот
                            же материальный мир, находясь в том же потоке закона совершенствования,
                            как носитель-фильтр (как аппарат, через который проявляется
                            несовершенная виджняна), совершенствуется сам. Поэтому мир имеет
                            упорядоченную динамическую структуру в пространстве и во времени.
                            Переход от низших форм организованности к высшим в материальном мире
                            обусловлен совершенствующим движением по восходящей линии, и этот
                            переход усложняет материальную систему не только в количественном, но и
                            в качественном отношении. Сама первоматерия (элементарные частицы) –
                            пункт, или мостик, через который виджняна-дух устанавливает связь с
                            материей, есть <strong>основа материального бытия</strong>. Нет природы
                            или материального вещества, которые бы не состояли из первоматерии. От
                            количества, состава, взаимосвязи и взаимодействия первоматерии зависит
                            конфигурация атомов (если мы берем за первоматерию электрон или более
                            мелкие частицы, которые существуют в сотую и тысячную доли секунды).
                            Орбиты и спины электронов этих частиц меняют химические и физические
                            качества элементов. Так как причинная связь первоматерии обусловлена
                            общим законом совершенствования, то в самой первоматерии заложена
                            рациональность, упорядоченность, стремление к развитию и усложнению,
                            сопротивление разрушению. И общий ход развития материального мира
                            поэтому строго соответствует всем этапам саморазвития виджняны.
                            Возникновение макроскопического мира (органического и неорганического)
                            есть деятельность закона причин и следствий в макромире, благодаря
                            которому произошло соединение атомов, различных химических соединений в
                            различных количествах и самых разнообразных пространственных
                            конфигурациях, то есть само внутреннее стремление первоматерии подчинено
                            закону совершенствования. В чувственном же макромире постоянное
                            стремление к развитию, к усложнению жизни закон причины и следствия
                            имеет только количественное значение, но не качественное; связь и
                            диалектические движения, установленные им, приобретают качественное
                            значение (особенно в духовном мире) благодаря закону вечного
                            совершенствования виджняны до слияния с Нирваной. Если
                            причинно-следственный закон устанавливает связи и создает движение, то
                            закон совершенствования порождает особую формообразующую энергию,
                            которая формирует макромир. Первый этап деятельности закона
                            совершенствования есть создание неорганической природы. А на следующем
                            этапе он создает органический мир.</p>
                        <p>Не вступая в противоречие с наукой, буддисты подходят к этому вопросу
                            так. Не затрагивая современную космологическую теорию об образовании
                            Вселенной и различных свойств отдельных галактик, мы берем обособленно
                            нашу планету. В истории нашей планеты были времена, когда живой материи
                            на Земле не было. В условиях раскаленного газообразного состояния Земли
                            не могли существовать белковые соединения. Вероятно, не было даже
                            условий для образования химических соединений. Так как химические
                            элементы находились в свободном газообразном состоянии, они
                            располагались слоями по плотности. Поэтому при остывании Земли ее
                            внутренние части составились из тяжелых металлов и углерода. Остывая,
                            углерод вступал в соединение с тяжелыми металлами, образуя карбиды
                            металлов. При остывании на возникшей Земле водяных паров появились
                            первые углеводороды. Углеводороды соединялись с кислородом, давали
                            альдегиды, спирты и т.д. Альдегиды вступали в соединения с группами,
                            содержащими азот, которые, в свою очередь, соединялись с цианами
                            (СN)<sub>2</sub>, дав очень сложные химические (органические)
                            соединения, в состав которых входили углерод, кислород, водород и азот
                            (элементы органогенные). Такие органические соединения могли возникнуть
                            в первобытных водоемах. Таким путем, вероятно, возникали первые
                            органические соединения, приближающиеся к типу углеводородов и белков,
                            составляющих наиболее существенную часть живой протоплазмы. Если
                            проследить весь ход создания органической материи на Земле, то мы можем
                            представить великую, сложнейшую лабораторию, которой руководит
                            гениальный разум. Безусловно, случайность образования этих элементов от
                            изменения орбиты и спина электронов и случайность соединения этих
                            элементов между собой, благодаря чему образовалась органическая и
                            неорганическая природа, невозможно представить.</p>
                        <p>Во всей истории возникновения природы (Сансары) нам ясно видно неутомимое
                            творческое искание великого закона совершенствования Алая-виджняны
                            вплоть до слияния с Нирваной. причем эта творческая сила комбинировала
                            атомы таким образом, чтобы только обеспечить образование природы, не
                            больше и не меньше. При этом закон причины и следствия в руках великого
                            совершенствования действует, как слепое орудие, как механизм в руках
                            человека. Как мы указали, на Земле органическая материя появилась
                            благодаря множеству комбинаций и соединений разнообразных химических
                            элементов. Эти комбинации, усложняясь, образовали зеленые одноклеточные
                            организмы, от которых пошло развитие двух основных стволов живой природы
                            - мир растений и мир животных. Причины и следствия создали различия в
                            строении и в жизненных потребностях у этих организмов, а возникшая между
                            ними борьба за существование с выживанием наиболее приспособленных
                            должна была привести к дивергенции, к обособлению растительных и
                            животных организмов, различающихся между собой, главным образом, по
                            способу питания и по типу обмена веществ. Борьба за существование в
                            первичных организмах есть первое проявление несовершенства
                            Алая-виджняны. Растения питаются за счет воды с растворенными в ней
                            солями и за счет углекислого газа; животные - уже готовыми органическими
                            веществами, образовавшимися в теле растений. Растения - созидатели
                            органического вещества. Животные - его потребители. Несмотря на то что
                            обмен веществ у растений и животных происходит различными путями, эти
                            два типа оказываются приспособленными друг к другу. Конечные продукты
                            обмена одних форм служат исходным пищевым материалом для других. Этим
                            путем устанавливается биологический обмен веществ, многократное
                            использование важнейших элементов для построения живых организмов.
                            Дальнейшее развитие живых организмов эволюционным путем происходит через
                            червей, моллюсков, иглокожих, хордовых и т.д. и доходит до позвоночных.
                            И дальше эволюция идет благодаря естественному отбору по самым
                            разнообразным путям, образовывая великое множество разнообразнейших
                            классов, групп, отрядов животных на Земле. Сам естественный отбор
                            обусловлен причинно-следственным законом, само стремление и переход от
                            низших к высшим идет по закону совершенствования.</p>
                        <p>С образованием животных Алая-виджняна индивидуализируется и приобретает
                            вместилище и форму в скандхах животных. Через них, то есть через эти
                            тела, живые существа получают возможность актуализировать свое
                            несовершенство в окружающем мире в борьбе за существование. Образование
                            животного организма есть обретение Алая-виджняной определенной
                            индивидуальной формы. Вместе с тем в теле каждого животного появляется
                            великое множество нервных клеток, в центре которых находится невидимый
                            нервный стержень, который проходит вдоль всего тела от коры головного
                            мозга до конца полового члена, на этом стержне расположены пять узлов,
                            стержень носит на санскрите название <strong>авадхути</strong> (или
                            <strong>авандуди</strong>). Практически в каждом животном организме он
                            не действует. На пяти узлах указанного стержня условно сосредоточены
                            эмоции, чувства и нервы, которые у несовершенной Алая-виджняны
                            естественно реализуют грешные поступки, вернее, они суть источники
                            грехов и называются <strong>клешами</strong>. Эти эмоции и чувства
                            диктуют свои желания в виде инстинктов тем чувствительным нервам, через
                            которые животные имеют общение с внешним миром. У низших животных клеши
                            реализуются неполно. По мере развития (совершенствования) организма
                            развиваются и дифференцируются различные отделы и органы тела, и вместе
                            с тем развивается их психическая деятельность. По мере развития высшей
                            нервной деятельности животное все глубже проникает в окружающий его мир.
                            Это проникновение характеризуется проявлением клеш. Развитие и
                            совершенствование животных эволюционным путем происходит по строгому
                            подбору, определяемому законом совершенствования. Не все животные
                            развивались одинаковыми темпами, хотя они и находились в одинаковых
                            условиях жизни.</p>
                        <p>Если взять происхождение человека по Дарвину, то мы увидим следующие пути
                            развития. «Человек,- пишет Дарвин, - является отпрыском обезьяньего
                            ствола Старого Света, и с точки зрения генеалогии, он должен быть
                            помещен с узконосыми обезьянами». Если человек произошел от древней
                            формы человекообразных обезьян третичного периода, исчезнувшей с Земли в
                            процессе эволюции или по другим причинам, то нужно полагать, что эти
                            обезьяны в свое время лазали по деревьям; между тем зоологи утверждают,
                            что птицы от лазающего древнего образа жизни (юрские первоптицы) перешли
                            к воздушному передвижению и к связанным с ним способам добывания корма
                            (пернатые). Обезьяны же, хотя они так же лазали по деревьям, как и
                            первоптицы, не обнаружили склонности к полету, а спустились на Землю.
                            Кроме того, несмотря на то, что все животные развивались в более или
                            менее одинаковых условиях, все-таки обезьяны среди них выделялись
                            высоким развитием высшей нервной деятельности (например, опыты
                            Павлова).</p>
                        <p>Если действительно обезьяны способны в большей мере, чем другие животные,
                            искать и находить обходные пути к пище (опыт с обезьяной Рафаэлем и
                            бананами), пользоваться различными предметами как орудиями для
                            доставания приманки, способны в отдельных случаях не только употреблять
                            готовые предметы, но даже приспосабливать и комбинировать их для
                            определенных целей, то это указывает на более высокую степень
                            совершенства высшей нервной деятельности обезьян среди других животных.
                            (О дельфинах, у которых мозг почти такой же, как у людей, речь будет
                            идти отдельно). Чем это объясняется? Какие условия жизни способствовали
                            этому развитию? На этот вопрос буддизм отвечает так.</p>
                        <p>Закон совершенствования (законом мы называем его потому, что развитие
                            природы идет на наших глазах неумолимо вперед по восходящей линии и
                            закономерно) есть стремление несовершенного духа к Нирване путем
                            просветления. С появлением животных на Земле Алая-виджняна
                            дифференцируется и обособляется в материальном теле. Поэтому ядро всего
                            живого, являясь несовершенной виджняной, имеет по природе своей
                            стремление к совершенству, то есть каждое животное обладает способностью
                            саморазвития, стремится к осуществлению высшей формы организации.</p>
                        <p>Само непосредственное развитие идет по <strong>закону кармы</strong>
                            (причины и следствия), который проявляется в виде естественного отбора.
                            Но внутренний импульс у разных животных идет по-разному в зависимости от
                            условий и особенно от активности проявления клеш. В данном случае
                            внутреннее развитие имело более благоприятные условия именно у третичных
                            обезьян. Закон совершенствования (внутренняя энергия плюс стремление к
                            совершенству) из миллионов разновидностей животных выбрал эту древнюю
                            обезьяну и направил ее развитие не в сторону полета, как юрских
                            первоптиц, а в сторону развития высшей нервной деятельности и
                            психической жизни. Ведь первый человек, когда он подошел к палке или
                            камню и использовал их как орудие труда, имел уже высокоразвитые нервную
                            систему и психическую деятельность, коль скоро он разобрался в
                            полезности этих орудий и стал применять, уже комбинируя их. Бобры делают
                            удивительно сложные жилища, так же, как и муравьи, пчелы и пауки, но
                            они, однако, не пошли дальше этих инстинктов. Если бытие определяет
                            сознание, то бобр строительством своих жилищ мог бы определить свое
                            сознание; поскольку этого не случилось, мы утверждаем, что в развитии
                            человека главенствующую роль сыграл великий божественный закон
                            совершенствования, направляя его в сторону развития высшей нервной
                            деятельности и психической жизни.</p>
                        <p>Утверждение Энгельса, что труд создал человека, не может дать ответа на
                            вопрос: какими экспериментами или трудами пришлось заниматься дельфину,
                            для того чтобы иметь такой развитый мозг? Если судить по данным
                            некоторых исследователей, дельфины имеют свой язык и способны осваивать
                            человеческий язык. В море они пасут рыб и других морских животных на
                            сытных хороших пастбищах, а через определенное время загоняют их обратно
                            в гроты. Одним словом, они ведут себя с рыбами, как люди со скотом на
                            земле. Природа дельфинов нам непонятна. В истории Нагарджуны, одного из
                            основателей Махаяны, сказано, что Будда проповедовал учение
                            «Праджняпарамита» не людям, а нагам, то есть духам воды. После Будды
                            явился на Землю Нагарджуна, который созерцал Нага в течение 17 лет,
                            погрузился в воду, то есть в страну нагов, и получил там
                            «Праджняпарамиту». Кто такие наги - духи воды? Не связаны ли они с
                            дельфинами?</p>
                        <p>Дальнейшее развитие, безусловно, идет через естественный отбор,
                            обусловленный законом причин и следствий (карма). Можно было бы
                            возражать в том случае, если бы бобр имел сознание. Но его природа не
                            соответствует такому развитию, как у человека. Физическая конструкция
                            его тела не имеет соответствующих деталей для углубленного изучения
                            природы (например, нет пальцев, не ходит на четырех ногах, может
                            работать только челюстями). Здесь ясно видно то закономерное и разумное
                            развитие, которым руководит Великий закон совершенствования, а именно:
                            естественный отбор направил древнюю обезьяну - человека по заранее
                            намеченным путям. Внутреннее развитие (развитие высшей нервной
                            деятельности и психической жизни) идет параллельно с развитием
                            соответствующих физиолого-анатомических органов (органов чувств и т.д.).
                            Мы не отрицаем большого значения окружающей природы для развития живого
                            организма, вплоть до человека. Все развитие природы идет по строго
                            намеченному пути, поэтому одно соответствует другому. Сложное
                            переплетение причинной связи развивает весь феноменальный мир таким
                            образом, что одно не может существовать и развиваться без другого.
                            Растения не могут жить без мертвой природы, без химических элементов, а
                            животные не могут жить без растений. Сами химические элементы не могут
                            образовываться и иметь те качества, при комбинации которых образуются
                            химические вещества, так же как и качества, полезные для питания
                            растений, без соответствующих качеств, движений и спин электронов. А
                            движение электронов, их орбита и спин, как основа и причина образования
                            всего феноменального мира, было необходимо обусловлено вечным
                            стремлением и движением Алая-виджняны к Нирване. В противном случае, то
                            есть если бы электрон имел не закономерное, а хаотическое движение, мы
                            не могли бы иметь наш прекрасный мир и сознательного современного
                            человека.</p>
                        <p>Таким образом, закон совершенствования не только вложил в первоматерию
                            способность развиваться и образовывать невероятные количества
                            разнообразных элементов, но и вложил в нее способность к ощущению,
                            которая раскрывается на определенном этапе развития тела живого
                            организма (это есть тот мостик, через который материя связывается с
                            духом), и способность образовывать, таким образом, сложную форму,
                            обеспечивающую актуализацию и проявление несовершенного духа.</p>
                        <p>Будучи индивидуализирована, Алая-виджняна проявляет свои клеши, создавая
                            бесконечно сложные кармы в борьбе за существование. Определенное
                            количество времени (перерождение) Алая-виджняна пребывает в одном живом
                            теле, а потом, в зависимости от кармы, переходит в другое животное и
                            т.д. Наконец, на высшей ступени процесса саморазвития виджняна обретает
                            сознательность в человеческом индивиде. Алая-виджняна, как частица
                            Адибудды, на всех этапах своего развития сохраняет полную непроявленную
                            форму божественной мудрости (ye shes), которая частично проявляется в
                            виде интуиции (shes rab). Она связана с психонервным процессом, в центре
                            которого действует авандуди. Авандуди, как мы указали раньше, расположен
                            в виде нервного стержня вдоль всего туловища человека (от коры головного
                            мозга до конца полового органа). Вдоль авандуди расположены пять узлов:
                            верхний узел находится в коре головного мозга, следующий расположен у
                            горла, третий - на сердце, четвертый - у пупа (ниже пупа на два пальца),
                            последний - у полового органа. Но у несовершенной Алая-виджняны, в
                            отличие от Адибудды, все узлы авандуди покрыты мраком клеш в виде эмоций
                            (неведение, желание, страсть, гнев, гордость и зависть). В области
                            верхнего узла, который называется <strong>колесо великого
                                блаженства</strong> (bde ba chen po, bde-chen gyi ‘khor-lo,
                            mahāsukhacakra), находится мозг, где раскрывается сознание. Это и есть
                            образование второй сигнальной системы (по И.П.Павлову). Благодаря
                            появлению сознания субстанция обратилась в субъект, необходимость
                            превратилась в свободу. С этого момента совершенствующее движение
                            Алая-виджняны сопровождается качественным изменением. У индивида в
                            феноменальном мире, в отличие от бессознательных животных, действуют все
                            клеши, начиная с неведения, которое порождает все остальные клеши в виде
                            эмоций. Если у животных действуют неведение и желание ('dod khams) как инстинкт любви к самому себе и к продолжению своего рода в
                            виде любви к своим детям и половой любви, то у человека любовь
                            приобретает гораздо более широкое и качественно иное значение.
                            Человеческая любовь порождается неведением, ибо она обусловлена
                            двойственностью природы вещей (прекрасное и безобразное, хорошее и
                            плохое и т.д.). А двойственность в Сансаре исчезает с исчезновением
                            неведения. Любовь же порождает или активизирует клеши - гордость и
                            зависть (ревность). Стимулы жизни, то есть борьба за существование,
                            является проявлением указанных клеш. Кажется, что любовь к детям,
                            половая любовь и любовь к себе самому имеют разные корни, но на самом
                            деле они исходят из одного источника - сознания, утверждающего
                            существование индивидуального Я (<em>ego</em>) в каждом индивидууме.</p>
                        <p>Сознание наличия индивидуального Я создает мысли <em>мое</em>,
                            <em>чужое</em>, инстинкт самосохранения и сохранения своего рода, и
                            проявляется в виде любви к детям и половой любви, которая часто
                            побеждает трезвый разум у индивидуума. Во всех действиях человека и
                            животных сознательно и бессознательно выступают указанные клеши. Они
                            руководят поведением человека и животного в окружающем мире. Всякие
                            душевные явления, начиная от бессознательного инстинкта и кончая
                            психической жизнью, подчинены этим клешам. У низших животных они
                            действуют бессознательно, как инстинкт - ответная реакция и реакции,
                            направленные на утоление голода и половой потребности. Таким образом,
                            они, действуя как бессознательные инстинкты, обеспечивают жизнь низшим
                            животным организмам.</p>
                        <p>Они же, вступая в борьбу за жизнь во внешнем мире, отделяют неживую
                            природу от живой. Иначе говоря, животные становятся животными тогда,
                            когда они вступают в борьбу за существование. Вся эта борьба -
                            бессознательная, инстинктивная у животных и сознательная у человека -
                            исходит от внутреннего стремления жить, сохранить свое <em>ego</em> и
                            продлить свой род. Это стремление обусловлено совершенствующим движением
                            Алая-виджняны к Нирване. Внутренний импульс или жизненный порыв есть
                            основное свойство Алая-виджняны, а проявление ее во внешнем мире
                            происходит через условные рефлексы у животных, то есть через органы
                            чувств. Эти органы чувств производят как бы двойную работу: во-первых,
                            они познают окружающий мир, выясняют полезность или вредность тех вещей,
                            с которыми они соприкасаются. Во-вторых, они стараются использовать их
                            для себя в меру их полезности или вредности. В первом случае внешний мир
                            (вишая - yul) и органы чувств (индрия – dbang po) действуют одновременно
                            как источник всякого познания. В обоих случаях клеши проявляются через
                            органы чувств. В первом случае органы чувств (индрия) уходят во внешний
                            мир, как разведчики, для выяснения того, что окружает их, а вишая идет к
                            ним навстречу, показывая свое содержание. Во втором случае инстинкт
                            (сознание) получает сведения о внешнем мире и дифференцирует его на
                            вредное и полезное и принимает меры для своей безопасности там, где
                            встречаются вредные элементы, и для использования полезного в течение
                            своей жизни.</p>
                        <p>Таким образом, каждый живой индивидуум ведет себя во внешнем мире так,
                            чтобы обеспечить интересы несовершенных эмоций - желания Алая-виджняны.
                            Но все эти действия находятся в полной зависимости от закона кармы.
                            Поэтому в чувственном мире все поступки живых существ строго
                            детерминированы и необходимо вытекают из данного стечения обстоятельств.
                            Вернее, всякое действие животного в причинно-следственном ряду есть и
                            причина, и следствие: следствие по отношению к предыдущим причинам и
                            причина по отношению к будущему движению и следствию. Под влиянием
                            указанных условий, определяющих жизненный опыт животных в виде
                            инстинкта, формируется его эмпирический характер. Все эти условия, при
                            которых развивается земная жизнь - жизнь Сансары, есть условия
                            постоянного страдания несовершенной Алая-виджняны. На всех этапах своего
                            развития Алая-виджняна в живом существе вступает в беспрерывную борьбу.
                            В этой борьбе она никогда до конца не удовлетворяет свои желания
                            вследствие сопротивления, оказываемого ей другими живыми существами. Ее
                            жажда поэтому никогда не утихает. Это постоянное неудовлетворение
                            желания есть основное страдание несовершенной Алая-виджняны. Оно же
                            является стимулом дальнейшего ее развития. Эта борьба
                            индивидуализированных живых существ во внешнем мире и с внешним миром
                            развивается в причинно-следственной связи и поэтому зависит от
                            количества и качества проявленных животным несовершенств. Алая-виджняна
                            данного животного или переходит на высшую ступень, или падает еще ниже.
                            Каждая причина вызывает соответствующее по количеству и по качеству
                            следствие. Таким образом, Алая-виджняна, проходя через толщу Сансары, в
                            силу деяний по закону кармы значительно усложняет свои клеши. Так как
                            сама Алая-виджняна есть частица Нирваны (Адибудды), то она постоянно и
                            неуклонно стремится к ней. То падая, то поднимаясь, она вечно идет
                            вперед. Обретя человеческий облик, благодаря своему разуму и свободной
                            воле она имеет гораздо больше возможности совершенствоваться и имеет
                            столько же возможности грешить, а потому человек может пасть гораздо
                            ниже, чем бессознательное животное. Она снова падает в бездну, но снова
                            извлекается из бездны законом совершенствования. Этот круговорот
                            безначальный и не имеет конца, пока человек не осознает своего
                            назначения. Последнее происходит в том случае, если субъект, не
                            отделяясь от объекта, а воспринимая объект как феномен, переживает свою
                            реальность и осознает непосредственно реально: «Я как человек». Таким
                            образом получается сознательное бытие. Это есть условие возникновения
                            морального чувства. Здесь человек начинает различать, что добро
                            (хорошо), что не-добро ( плохо). На этом подробно остановимся ниже.</p>
                        <p>Если каждое животное (в широком понимании) имеет Алая-виджняну и каждый
                            индивид ведет борьбу за жизнь с окружающим миром, то значит, в основе
                            всего органического бытия лежит единая жизнь, единый жизненный поток,
                            который сводится к той же вечной борьбе за жизнь. В любом перерождении
                            Алая-виджняна в теле любого животного приобретает те же стремления, что
                            были у нее в предыдущих перевоплощениях, а так как перерождения
                            повторяются бесчисленное количество раз, то эти основные чувства в
                            предыдущих перерождениях (боязнь опасности, ответная реакция и др.)
                            сохраняются в самой Алая-виджняне, то есть указанные бессознательные
                            инстинкты суть свойства самих клеш. В результате бесчисленных количеств
                            повторений (в каждом перерождении Алая-виджняны) они вырабатываются, как
                            ассоциация, память или врожденные инстинкты. Одним из тех несовершенных
                            инстинктов, выступающих как эмоции, с которыми не может расстаться
                            Алая-виджняна до самой Нирваны, является сознательное и бессознательное
                            утверждение о наличии индивидуального Я, которое, как мы указали,
                            порождает 1) любовь к себе самому, к своему собственному Я, и 2) любовь
                            или желание продолжить свое потомство, которое проявляется как любовь к
                            своему активному началу, то есть к женщине, и любовь к своим детям.
                            Последние две любви эмоционально проявляются по-разному. Первая как
                            похоть, она направлена на удовлетворение половой потребности, которая
                            является методом рассады семени для продолжения потомства. Вторая -
                            скорее не любовь, а жалость к своим детям или смешанная эмоция любви и
                            жалости.</p>
                        <p>Любовь к себе самому есть инстинкт самосохранения. Она в течение
                            бесчисленного количества перерождений выработала все основные инстинкты
                            бессознательных животных. Таким образом, они становятся врожденными
                            свойствами всякого животного. Вот почему новорожденные поросята сразу же
                            бросаются к соскам матери и т.д. Ни одна биологическая или
                            физиологическая теория на сегодня не дает исчерпывающего ответа на
                            вопрос о появлении инстинкта у животных, ибо это относится к области
                            духа, а не материи. Если по закону кармы дух (виджняна) перевоплощается
                            из более низкой в более высокую организацию животного, то инстинкт ее
                            остается в тех же пределах, в каких находилась последняя форма животного
                            организма. Это объясняется тем, что условия действия (развития)
                            животного в окружающем мире дифференцированы, зависят от развития
                            конституции тела самих животных. Если он (дух) перейдет еще ниже (низшие
                            виды животных), то богатство ассоциаций (инстинкт) остается в
                            бездействии, ибо сама организация низших существ не допускает раскрытия
                            его. Но основные инстинкты - боязнь, защита, предосторожность -
                            действуют почти всюду.</p>
                        <p>Чем сложнее психическая жизнь и высшая нервная деятельность животных, тем
                            более дифференцированно развиваются эти инстинкты. Например, у человека
                            действуют все пять инстинктов в виде эмоций. Любовь к себе и к своему
                            собственному Я здесь принимает общественный и частный, моральный и
                            аморальный характер. Таким образом, наличие инстинктов у животных и
                            человека ясно показывает наличие перерождения несовершенного духа в
                            Сансаре. Из инстинкта <em>любви</em> вытекает еще очень важный инстинкт,
                            который впоследствии переходит в сознательный анализ - это инстинкт
                            непосредственного <strong>интуитивного</strong> <strong>познания чужой
                                одушевленности</strong> (у человека). Познание чужой одушевленности
                            есть способность каждого животного постигать в той или иной мере чужую
                            жизнь. Оно непосредственно связано с боязнью, ответной реакцией и т.д.
                            Поэтому в природе каждое животное отличает то, что сильнее его и потому
                            опаснее, и то, что слабее и полезнее. Конечно, если бы коршун не отличал
                            орла от гуся, он не мог бы существовать. Там, где сама жизнь становится
                            сознательной (человеческая жизнь), это постижение обращается в понимание
                            чужой психики.</p>
                        <p>Если ребенок в раннем возрасте начинает понимать мимику матери, ласкающую
                            или строгую, то это инстинкт, приобретенный его душой в процессе
                            различных перерождений; он исходит из инстинкта любви. Этот инстинкт
                            лежит в основе познания чужого Я. В сознательном возрасте человек
                            производит разумный, логический анализ чужой психики. Чем глубже он
                            проникает в чужую душу, тем больше он открывает в ней новых качеств.
                            Здесь он проводит необходимую аналогию с собой. На последнем этапе
                            познание чужого Я становится способом познания самого себя. Конечно, при
                            анализе чужой психики оказывается, что чем родственнее мне эта психика,
                            тем она понятнее, чем она дальше от меня, тем поверхностнее ее понимание
                            мною.</p>
                        <p>Указанный инстинкт, исходящий из инстинкта любви, направлен на познание
                            внешнего мира. На высших ступенях совершенствования у Алая-виджняны
                            открывается сознание – то есть головной мозг у человека и его свобода
                            воли. Ибо свобода воли есть необходимое качество сознания. Здесь
                            количество совершенствующих движений переходит в качество. Несовершенный
                            инстинкт любви, изменяясь количественно и качественно, превращается по
                            закону диалектики в свою противоположность. Инстинкт познания, исходящий
                            из инстинкта любви, переходит в познание разума, детерминированная
                            Алая-виджняна обретает относительную свободу. Дальнейшее
                            совершенствование происходит в зависимости от свободной воли
                            индивида.</p>
                        <p>Полная свобода духа реализуется лишь тогда, когда он направляет акты
                            сознания от окружающего мира на самого себя, когда дух (виджняна)
                            осознает свою субъективную сущность, не давая возможности через сознание
                            актуализоваться несовершенству клеш в феноменальном мире, обдумывая и
                            созерцая жалость – сострадание как предикат бодхисаттвовского
                            просветления, затем осознает и свою абсолютность как частицы Нирваны. Но
                            переход инстинкта познания в познание разума определяет свободу воли. То
                            есть, познающий разум в основу своего анализа кладет участие самого
                            человека в мире, не отделяя при этом объект от субъекта, но анализируя
                            их взаимоотношения и из них выводя самого человека как объект,
                            обеспечивающий саму реальность, а не явление. Таким образом, познающий
                            разум (субъект), воспринимая объект, сам переживает свою реальность,
                            сознает непосредственно и реально «Я как человек». Категория,
                            определяющая реальное сознание «Я как человек», устанавливает этим самым
                            сознательное бытие. Это значит, что человек узрел добро и зло, что плохо
                            и что хорошо. Таким путем инстинкт любви превращается в свою
                            противоположность - в познающий разум, и благодаря этому становится
                            категорией совершенствования.</p>
                        <p>Прежде всего следует учесть, что все живое, в том числе человек,
                            становится живым с того момента, когда вступает в тесную взаимосвязь с
                            внешним миром. Но круг действия отдельных живых существ строго
                            ограничен. Если взять животных, то для каждого отряда или вида животных
                            существует свой круг или диапазон действий во внешнем мире, в
                            зависимости от физиологической конструкции его тела. А у человека
                            гораздо больше диапазон действий, возможности познания в связи с
                            раскрытием в нем сознания. Но каждый индивид имеет свои собственные
                            способности познания, свой диапазон проникновения в мир в зависимости от
                            степени своего совершенства. Поэтому каждый индивид понимает чужое Я в
                            пределах своих возможностей. Это служит основой всех разногласий и
                            противоречий между людьми и полностью зависит от степени их
                            совершенства. Анализ этой ограниченности индивида приводит нас к
                            необходимости идеи терпимости, которая является основным элементом
                            добра. Несовершенный человек не может быть уверен в негреховности своего
                            поступка до тех пор, пока им руководят клеши, и потому он не может
                            бросить камень в развратницу - ведь греховность и преступность любого
                            индивидуума есть реультат его несовершенства и, стало быть,
                            ограниченности его познания (просветления), и его действия должны быть
                            прощены. (Это рассматривается иначе в социально-общественном плане).</p>
                        <p>Один из основателей экзистенциализма Хайдеггер (Хедегер) в своей книге
                            «Бытие и время», изд. 1929 г., утверждает, что в бытие человека
                            необходимо включать категорию времени. То есть решающую роль играет
                            временность, так как отношение человека к миру определяется временем:
                            время для материального мира (объекта) отличается от времени для
                            субъекта. Время для физического мира есть производное от него (то есть
                            вторичное), однородное, чисто количественное, временность же субъекта
                            есть озабоченность за свою судьбу.</p>
                        <p>По мнению Хедегера, 1) человек как бы брошен в мир из небытия, с его
                            рождением появляется его индивидуальность; 2) человек вместе с тем
                            чувствует свою конечность, то есть его жизнь есть путь, ведущий к
                            смерти; 3) в самом времени решающее значение имеет направленность к
                            будущему. Настоящее определяется будущим, прошедшее есть опыт, которым
                            он пользуется, чтобы устроить будущее. Эта забота о будущем первична. То
                            есть время - качественно. В этом заключается основная ситуация
                            человека.</p>
                        <p>На основании этих положений Хедегер рассматривает три вопроса.</p>
                        <p>1. Учитывает ли (сознает ли) человек всё, что заложено в него, как в
                            конечное существо, обреченное на смерть?</p>
                        <p>2. Как реализовать заложенные в него возможности?</p>
                        <p>3. Или человек не сознает своей конечности, или не хочет ее
                            сознавать?</p>
                        <p>Все эти вопросы были поставлены буддистами. Сам Будда постоянно учил и
                            предупреждал всех, что каждый человек обречен на гибель и потому пусть
                            торопится совершенствоваться. Великий Нагарджуна говорит в своей теории
                            о <strong>непостоянстве вещей</strong>, что «все в мире непостоянно»;
                            так же и человек, он - как свеча на поле, может погаснуть при малейшем
                            дуновении ветерка». А в следующем перерождении он может по закону кармы
                            найти бесконечно низкий живой организм и воплотиться в него, но там не
                            будет сознания и свободной воли, и он не сможет найти удобного пути для
                            совершенствования. Поэтому Нагарджуна призывал всех сознательных людей
                            осознать свою конечность, стремиться к совершенству беспрерывно.</p>
                        <p>Мы согласны с Хедегером, когда он рассматривает время как основную
                            категорию, определяющую субъект. Но когда человек, учитывая свою
                            конечность, старается осуществить заложенные в него возможности, он
                            сознает и осуществляет полное бытие, то есть он сознает свою силу и
                            рассчитывает на нее. Таким образом, он набирается решимости, надежды на
                            себя как на конечное существо, стремящееся извлечь из себя всё, что
                            возможно. Эта его решимость и есть его свобода. Здесь человек стоит на
                            стыке двух путей. Первый путь - это путь, ведущий к мирскому
                            наслаждению, власти, богатству, имени и т.д., куда он может идти,
                            используя большой опыт. Он идет через обман, ханжество, через кровь и
                            жестокость; из-за своего невежества он не осознал подлинного своего
                            назначения - назначения человека, высшей ступени совершенствующегося
                            духа. Второй путь - путь, ведущий к Нирване. Человек на этом пути
                            сознает свою сущность, сознает то, какой путь он прошел в течение
                            бесконечного, мучительно повторяющегося процесса перерождения в Сансаре
                            в силу осознания несовершенства своих деяний. Он не уверен, приобретет
                            ли в следующем перерождении опять такую благоприятную для
                            совершенствования сознательную и свободную человеческую форму, как
                            теперь. Также он сознает, что в Сансаре по причине совершенствующего
                            движения Алая-виджняны по закону кармы всё устроено изменчиво. Имя,
                            богатство, наслаждения - всё это преходяще, всё это изменчиво. В
                            конечном итоге понимает он, это есть прихоть его несовершенства.
                            Единственное постоянное - это Нирвана и совершенствующаяся
                            Алая-виджняна. Поэтому он торопится к совершенству, чтобы успеть
                            использовать благоприятный момент.</p>
                        <p>Есть и третий человек. Этот человек не сознает и не хочет сознавать свою
                            обреченность и конечность. Не хочет смотреть правде в глаза, сам себя
                            обманывает иллюзией. Не хочет брать на себя ответственность,
                            перекладывая ее на природу и на сознательное устройство мироздания. Он
                            не находит в себе решимости определить свой путь. Это есть упадочное
                            бытие. Хедегер говорит: здесь он перестает отличаться от объекта.
                            Поэтому философия так долго не могла отличить субъект от объекта. Первый
                            путь мы называем активно отрицательным бытием. Человек быстрым темпом
                            идет против всеобщего движения вперед. Третий же путь - упадочное бытие.
                            Если человек стоит на месте, он также тормозит всеобщее движение. Таким
                            образом, в мире нет этического нуля.</p>
